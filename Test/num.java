

public class num
{
    public static void main(){
        int num;
        num = 99 / 10;
        System.out.println(num);
        
        num = 99 / 10 * 10;
        System.out.println(num);
        
        num = 99 % 10;
        System.out.println(num);
        
        num = 99 - 99 % 10;
        System.out.println(num);
        
        num = 99 - 99 / 10;
        System.out.println(num);
        
        num = 99 / 10 * 10 + 99 % 10;
        System.out.println(num);
    }
}
